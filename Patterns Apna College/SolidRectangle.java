public class SolidRectangle {
    public static void main(String[] args) {
        int row = 4;
        int cols = 5;
        for (int i = 0; i < row; i++) {
            for (int j = 0; j < cols; j++) {
                System.out.print("*" + " ");
            }
            System.out.println();
        }
    }
}